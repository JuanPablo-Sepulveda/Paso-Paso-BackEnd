// Un "web server" (servidor web) es un software diseñado para recibir, procesar y responder a las solicitudes de los clientes a través de internet. Es una pieza fundamental en la infraestructura de la World Wide Web, ya que permite que los sitios web sean accesibles a través de la red y entreguen contenido a los usuarios.
// Cuando un usuario ingresa una URL (Uniform Resource Locator) en su navegador web, este envía una solicitud al servidor para acceder a la página o recurso asociado con esa dirección. El servidor web es responsable de recibir esa solicitud, procesarla y enviar de vuelta una respuesta al cliente.
// Las funciones principales de un servidor web son:
// Recepción de solicitudes: El servidor web está constantemente escuchando las peticiones que llegan a través del protocolo HTTP (Hypertext Transfer Protocol) o HTTPS (HTTP Secure) en caso de que se requiera una conexión segura.
// Procesamiento de solicitudes: Una vez que el servidor web recibe una solicitud, debe interpretarla y determinar qué recurso está siendo solicitado (por ejemplo, una página HTML, una imagen, un archivo de audio, etc.).
// Obtención de recursos: Si el recurso solicitado es estático (como una imagen o un archivo CSS), el servidor simplemente lo envía al cliente. Si es dinámico (como una página web generada a partir de una base de datos), el servidor puede requerir la intervención de otro software, como un lenguaje de programación del lado del servidor (por ejemplo, PHP, Python, Ruby, entre otros), para generar la respuesta adecuada.
// Envío de respuestas: Una vez que el servidor ha obtenido el recurso solicitado, lo envía de vuelta al cliente a través de la conexión establecida. La respuesta generalmente incluye el recurso solicitado, información sobre el recurso (por ejemplo, cabeceras HTTP) y, si es necesario, códigos de estado para indicar si la solicitud fue exitosa o si ocurrió algún error.
// Algunos de los servidores web más conocidos y utilizados son Apache, Nginx, Microsoft Internet Information Services (IIS) y LiteSpeed. Cada uno tiene sus características y configuraciones específicas, pero todos comparten el propósito de servir contenido web de manera eficiente y segura a los usuarios que lo soliciten.

// Características de un servidor web
// Dentro de las características más destacadas de un servidor web se encuentran:

// 1. Seguro:
// Un servidor web debe ser seguro para poder proteger tus datos de accesos no autorizados o ataques malintencionados. También debe contar con medidas de protección frente a ataques de denegación de servicio (DoS) y otras ciberamenazas.

// 2. Rapidez:
// Los servidores web deben ser capaces de servir contenidos con rapidez, sobre todo cuando se trata de grandes volúmenes de datos.

// 3. Escalabilidad:
// Un servidor web debe ser capaz de gestionar las crecientes demandas de un sitio web o una aplicación en línea en expansión. Debe tener recursos y capacidad suficientes para atender a más usuarios sin sacrificar el rendimiento.

// 4. Fiabilidad:
// El servidor web debe ser fiable para que permanezca operativo durante largos periodos de tiempo. Debe ser capaz de gestionar picos de tráfico y peticiones de los usuarios sin bloquearse ni dejar de estar disponible.

// 5. Alta capacidad:
// Un servidor web debe ser capaz de procesar una gran cantidad de datos, como archivos HTML, imágenes, vídeos y otros contenidos.

// Elementos de un servidor web
// Para que un servidor web funcione correctamente, debe contar con los siguientes elementos:

// Sistema Operativo
// El sistema operativo tiene la tarea de gestionar los componentes de hardware y software de un servidor web. Los sistemas operativos más utilizados en los servidores web son Windows Server y distribuciones de Linux como CentOS, Ubuntu, Red Hat Enterprise Linux y SUSE Linux Enterprise Server.

// Sistema de archivos
// El sistema de archivos es el responsable de almacenar y organizar los datos en el servidor web. Incluye componentes como discos duros, discos, matrices RAID y otros dispositivos de almacenamiento.

// Aplicación de servidor web
// Una aplicación de servidor web sirve de interfaz entre el sistema operativo y el contenido que almacena. Las aplicaciones más utilizadas en los servidores web son Apache HTTP Server y Microsoft IIS.

// Servidor de base de datos
// Un servidor de base de datos almacena los datos de la aplicación, como cuentas de usuario y contraseñas, en un sistema de base de datos relacional. Las bases de datos más utilizadas en servidores web son MySQL, Oracle Database y Microsoft SQL Server.

